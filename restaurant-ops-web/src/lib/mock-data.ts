export function createInitialState(): RestaurantStoreState {
  const venueId = "venue-demo";
  return {
    products: [...products],
    categories: [...productCategories],
    customers: [...customers],
    printerJobs: [...printerJobSeed],
    tables: tableSeed.map((table) => {
      const withLog = addLog(table, {
        type: "seed",
        title: "Masa acildi",
        description: `${table.name} mock veri ile hazirlandi.`,
        severity: "info",
      });
      return {
        ...withLog,
        venueId,
        activeSessionId: `${table.id}-session`,
        version: 1,
        status: deriveTableStatus(withLog),
      };
    }),
    meta: {
      venueId,
      source: "mock",
      fetchedAt: new Date().toISOString(),
      snapshotVersion: "snapshot-demo-1",
      conflictStrategy: "server-wins",
      optimisticEnabled: true,
    },
  };
}
