const productMap = Object.fromEntries(products.map((product) => [product.id, product]));

function line(
  productId: string,
  overrides: Partial<OrderItem> = {},
): OrderItem {
  const product = productMap[productId];
  return buildProductOrderItem(product, overrides);
}

function serviceChild(
  productId: string,
  quantity: number,
  plateNumber: number | null,
  note = "",
  modifiers: string[] = [],
): ServiceChildItem {
  const product = productMap[productId];
  return {
    id: `${productId}-${plateNumber ?? "main"}-${quantity}`,
    productId,
    name: product.name,
    quantity,
    unitPrice: product.price,
    totalPrice: product.price * quantity,
    plateNumber,
    note,
    modifiers,
  };
}

function serviceLine(
  productId: string,
  structure: "standard" | "1_plate" | "2_plate" | "3_plate" | "4_plate" | "5_plate",
  items: ServiceChildItem[],
  orderName: string,
  note: string,
): OrderItem {
  const product = productMap[productId];
  return line(productId, {
    name: orderName,
    kind: "service",
    unitPrice: 0,
    customizations: emptyCustomizations({ note }),
    service: {
      serviceName: product.name,
      orderName,
      structure,
      plateCount: structure === "standard" ? 0 : Number(structure[0]),
      note,
      items,
    },
  });
}

function order(
  id: string,
  label: string,
  status: OrderStatus,
  items: OrderItem[],
  updatedAt: string,
): TableOrder {
  const tableOrder: TableOrder = {
    id,
    label,
    status,
    items: items.map((item) => ({ ...cloneOrderItem(item), status })),
    totalPrice: 0,
    createdAt: updatedAt,
    updatedAt,
  };
  tableOrder.totalPrice = calculateOrderTotal(tableOrder);
  return tableOrder;
}

function splitPlan(parts: BillSplitPlan["parts"], createdAt: string): BillSplitPlan {
  return {
    id: `split-${createdAt}`,
    mode: "product",
    createdAt,
    parts,
    note: "Iki kisi icin urun bazli plan.",
  };
}

