create policy "user_live_locations_own_all"
on public.user_live_locations
for all
to authenticated
using (auth.uid() = user_id)
with check (auth.uid() = user_id);

drop policy if exists "user_product_interests_own_all" on public.user_product_interests;
create policy "user_product_interests_own_all"
on public.user_product_interests
for all
to authenticated
using (auth.uid() = user_id)
with check (auth.uid() = user_id);

